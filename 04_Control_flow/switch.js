// sitch case for number datatypes

const month = 3

switch (month) {
    case 1:
        console.log("januray");
        break;
    case 2:
        console.log("Febuary");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;

    default:
        console.log("if no case match then it will print");
        break;
}

// witch case for string data type 

const months = "dec"

switch (months) {
    case "jan":
        console.log("jan-01")
        break;

    case "feb":
        console.log("feb-02")
        break;

    case "march":
        console.log("march-03")
        break;

    case "april":
        console.log("april-04")
        break;

    case "may":
        console.log("may-05")
        break;

    case "june":
        console.log("june-06")
        break;

    default:
        console.log(" if any case not matched then it will print");
        break;
}

